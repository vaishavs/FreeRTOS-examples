static void led_all_function(void* params)
{
	while(1)
	{
		status = xTaskNotifyWait(0,0,NULL,pdMS_TO_TICKS(1000));
		if(status == pdTRUE){
			portENTER_CRITICAL();
			HAL_GPIO_TogglePin(GPIOD, LD4_Pin); //Green LED
			HAL_GPIO_TogglePin(GPIOD, LD3_Pin); //Orange LED
			HAL_GPIO_TogglePin(GPIOD, LD5_Pin); //Red LED
			HAL_GPIO_TogglePin(GPIOD, LD6_Pin); //Blue LED
			portEXIT_CRITICAL();
			printf("All LEDs toggled!\n");
		}

	}
}

static void button_handler(void* parameters)
{
	uint8_t btn_read = 0;
	uint8_t prev_read = 0;
	BaseType_t pxHigherPriorityTaskWoken = pdFALSE;

	while(1){

		btn_read = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0);

		if(btn_read)
		{
			if(! prev_read) {
				xTaskNotifyFromISR(led_all,0,eNoAction,&pxHigherPriorityTaskWoken);

				/* once the ISR exits, the below macro makes higher priority task which got unblocked to resume on the CPU */
				portYIELD_FROM_ISR(pxHigherPriorityTaskWoken);
				printf("Button pressed!\n");
			}
		}
		prev_read = btn_read;
		vTaskDelay(pdMS_TO_TICKS(10));

	}

}
